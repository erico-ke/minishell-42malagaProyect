#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	signo;

	i = 0;
	result = 0;
	signo = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signo *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - '0');
		i++;
	}
	return (result * signo);
}
