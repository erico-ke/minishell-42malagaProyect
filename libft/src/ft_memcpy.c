#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	if (!dest && !src)
		return (0);
	while (n-- > 0)
	{
		*(char *)(dest + n) = *(char *)(src + n);
	}
	return (dest);
}
